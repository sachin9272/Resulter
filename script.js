function getUserDetails() {
    const rollNo = document.getElementById('rollNo').value;
    const userResult = document.getElementById('userResult');
    const error = document.getElementById('error');
    const button = document.getElementById('button');

    // Reset previous results
    userResult.style.display = 'none';
    error.style.display = 'none';

    if (users[rollNo]) {
        const user = users[rollNo];
        userResult.innerHTML = `
                    <strong>Name:</strong> ${user.name}<br>
                    <strong>Roll No:</strong> ${rollNo}<br>
                    <strong>Date of Birth:</strong> ${user.dob}<br>
                    <strong>Course:</strong> ${user.course}
                    <br>
                `;
        button.innerHTML = `
                    <a href="https://erp.aktu.ac.in/WebPages/OneView/OneView.aspx?AspxAutoDetectCookieSupport=1"> <button>Check Result</button>  </a>`
        userResult.style.display = 'block';
    } else {
        error.textContent = "Roll No not found!";
        error.style.display = 'block';
    }
}
